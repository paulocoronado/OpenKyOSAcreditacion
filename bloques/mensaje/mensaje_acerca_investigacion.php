            <p align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>PLAN 
              DE DESARROLLO DEL CENTRO DE INVESTIGACIONES Y DESARROLLO CIENT&Iacute;FICO 
              CIDC (2001 &#8211; 2005)</strong></font> </p>
            <p> </p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              En el contexto de la Universidad Distrital Francisco Jos&eacute; 
              de Caldas, el Centro de Investigaciones y Desarrollo Cient&iacute;fico, 
              CIDC, representa la unidad acad&eacute;mica y administrativa responsable 
              de la ejecuci&oacute;n de las pol&iacute;ticas dirigidas al cumplimiento 
              de la actividad de investigaci&oacute;n. En tal sentido, su misi&oacute;n, 
              visi&oacute;n, objetivos y funciones espec&iacute;ficas, expresan 
              la raz&oacute;n de ser de esta dependencia y determinan el horizonte 
              de trabajo y las metas hacia las cuales se dirigen las acciones, 
              estrategias y proyectos institucionales. </font></p>

            <p> </p>
            <p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>OBJETIVOS: 
              <br>
              <hr class="hr_division"> 
              </strong> </font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
              Elaborar, proponer y presentar al Consejo Acad&eacute;mico las pol&iacute;ticas 
              de Investigaci&oacute;n a desarrollar cada cinco (5) a&ntilde;os, 
              en concordancia con el Proyecto Universitario Institucional y con 
              los planes de acci&oacute;n de las facultades. <br>

              Promover la interrelaci&oacute;n entre la Universidad y los distintos 
              sectores del Distrito Capital y del pa&iacute;s. <br>
              Fomentar, coordinar, gestionar, evaluar y divulgar los proyectos 
              de investigaci&oacute;n y las actividades derivadas de tales procesos. 
              <br>
              Apoyar el trabajo acad&eacute;mico e investigativo de docentes y 
              estudiantes en todas sus modalidades.Estimular la formaci&oacute;n 
              de grupos de Investigaci&oacute;n de car&aacute;cter institucional 
              e interinstitucional de alta calidad. </font></p>
            <p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>

              <strong>FUNCIONES</strong>:<br>
              <hr class="hr_division"></font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Proponer 
              al Consejo Acad&eacute;mico las pol&iacute;ticas que en materia 
              de investigaci&oacute;n se requieran para consolidar los planes 
              institucionales.Apoyar el desarrollo de las l&iacute;neas y proyectos 
              de investigaci&oacute;n generados en las Facultades y los Proyectos 
              Curriculares en los niveles de formaci&oacute;n de pregrado y postgrado. 
              <br>
              Evaluar la calidad de las l&iacute;neas y los proyectos de investigaci&oacute;n 
              a trav&eacute;s del Comit&eacute; de Investigaciones del Centro 
              de Investigaciones y Desarrollo Cient&iacute;fico. </font></p>

            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Incentivar 
              la consolidaci&oacute;n de la comunidad acad&eacute;mica e investigativa 
              mediante la creaci&oacute;n y el fortalecimiento de grupos de investigaci&oacute;n 
              de car&aacute;cter institucional e interinstitucional. </font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Impulsar 
              la realizaci&oacute;n de convenios y alianzas estrat&eacute;gicas 
              con el sector productivo y otros sectores sociales. Realizar seminarios, 
              talleres y actividades acad&eacute;micas tendientes a la formaci&oacute;n 
              de investigadores y equipos de investigaci&oacute;n. <br>
              Desarrollar planes dirigidos a consolidar un sistema de informaci&oacute;n 
              de la investigaci&oacute;n, la construcci&oacute;n de redes de investigaci&oacute;n 
              y la dotaci&oacute;n de una infraestructura f&iacute;sica adecuada 
              para tales fines.</font></p>

            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Promover 
              la publicaci&oacute;n de los resultados de la investigaci&oacute;n 
              en medios audiovisuales de prestigio y/o a trav&eacute;s de convenio 
              con universidades nacionales y extranjeras. </font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              Para el logro de estos prop&oacute;sitos el Centro de Investigaciones 
              y Desarrollo Cient&iacute;fico CIDC se enmarca en los Ejes, Macropol&iacute;ticas 
              y Macroproyectos contenidos en el Plan de Desarrollo 2001-2005. 
              </font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              En consecuencia, adelanta sus acciones dentro del Eje de Desarrollo 
              MEJORAMIENTO DE LA ACADEMIA PARA EL DESARROLLO DE LA CIENCIA, LA 
              T&Eacute;CNICA, LA EDUCACI&Oacute;N Y EL ARTE EN EL CONTEXTO DE 
              LA SOCIEDAD Y LA CULTURA. Esto, teniendo en cuenta que &#8220;como 
              centro del saber y del aprendizaje especializado, a la Universidad 
              se le ha encomendado la tarea de construir, generar, producir y 
              difundir conocimiento de alto nivel y aplicarlo a la soluci&oacute;n 
              de los problemas del hombre y la sociedad. De esta manera, la ciencia 
              y otras formas del conocimiento posibilitan, adem&aacute;s de interpretar 
              y comprender la realidad, fundamentalmente transformarla, poniendo 
              en juego las capacidades que las experiencias de formaci&oacute;n 
              profesional proporcionan para la acci&oacute;n social.&#8221; </font></p>

            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              A su vez, en el Plan de Desarrollo Institucional se determina la 
              Macropol&iacute;tica 2: Calidad Acad&eacute;mica Pertinencia y Competitividad 
              al interior de la cual se contempla el Programa 3: FORTALECIMIENTO 
              DE LA INVESTIGACI&Oacute;N PARA EL DESARROLLO DE LA CIENCIA, LA 
              TECNOLOG&Iacute;A, LA EDUCACI&Oacute;N Y EL ARTE EN EL CONTEXTO 
              DE LA SOCIEDAD Y LA CULTURA. </font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              En este Programa se &#8220;sit&uacute;a la funci&oacute;n universitaria 
              de la investigaci&oacute;n como elemento fundamental para el cumplimiento 
              de las pol&iacute;ticas y metas institucionales. En el plano end&oacute;geno, 
              se busca incidir en la calidad educativa articulando la investigaci&oacute;n 
              a las propuestas de formaci&oacute;n profesional para generar una 
              cultura de indagaci&oacute;n de la realidad y b&uacute;squeda sistem&aacute;tica 
              de respuesta a los distintos interrogantes que su comprensi&oacute;n 
              y transformaci&oacute;n plantea. En el plano ex&oacute;geno se trata 
              de hacer que la investigaci&oacute;n adquiera sentido en las soluciones 
              que la producci&oacute;n de conocimiento logre dar a los problemas 
              vitales de la existencia del hombre y la sociedad en el contexto 
              del mundo contempor&aacute;neo, globalizado, diverso y complejo.&#8221; 
              </font></p>

            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              En tal sentido, el Programa 3: FORTALECIMIENTO DE LA INVESTIGACI&Oacute;N 
              PARA EL DESARROLLO DE LA CIENCIA, LA TECNOLOG&Iacute;A, LA EDUCACI&Oacute;N 
              Y EL ARTE EN EL CONTEXTO DE LA SOCIEDAD Y LA CULTURA traza los siguientes 
              objetivos para la instituci&oacute;n: </font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              Creaci&oacute;n y puesta en marcha de un sistema universitario de 
              Ciencia y Tecnolog&iacute;a, fundamentado en las interacciones entre 
              sus actores, las relaciones con el entorno local, nacional e internacional 
              y en la articulaci&oacute;n con el Sistema de Ciencia y Tecnolog&iacute;a 
              Nacional y el Distrito Capital. <br>

              Generaci&oacute;n de una cultura institucional de la investigaci&oacute;n 
              y el desarrollo de la capacidad de innovaci&oacute;n como par&aacute;metros 
              de calidad de la Educaci&oacute;n Superior en &iacute;ntima relaci&oacute;n 
              con las funciones universitarias de docencia y extensi&oacute;n. 
              <br>
              Acci&oacute;n investigativa en las distintas unidades acad&eacute;micas 
              ligadas al Proyecto Institucional y al Plan de Desarrollo como fundamento 
              de la identidad institucional y la proyecci&oacute;n social y cultural. 
              <br>

              Mejoramiento de los procesos de apoyo, formaci&oacute;n, desarrollo 
              y divulgaci&oacute;n de la actividad investigativa. <br>
              Articulaci&oacute;n de la investigaci&oacute;n cient&iacute;fica, 
              tecnol&oacute;gica, educativa y art&iacute;stica con el entorno, 
              asumiendo como par&aacute;metros de calidad los de competitividad, 
              productividad y desarrollo institucional. </font></p>
            <p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><br>
              Este Programa se configura a trav&eacute;s de cinco (5) Macroproyectos, 
              cada uno con sus respectivos proyectos para un total de diez (10), 
              desde los cuales se trazan los objetivos y se concretan las acciones 
              con base en estrategias flexibles, acordes con las necesidades de 
              la investigaci&oacute;n, las demandas de la sociedad y el grado 
              de desarrollo y consolidaci&oacute;n de la comunidad investigativa. 
              </font></p>

